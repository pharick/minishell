#include "libft.h"

t_list	*ft_lst_at(t_list *lst, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		lst = lst->next;
		i++;
		if (!lst)
			return (NULL);
	}
	return (lst);
}
