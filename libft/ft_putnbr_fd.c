#include <unistd.h>
#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	d;

	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else if (n < 0)
	{
		write(fd, "-", 1);
		ft_putnbr_fd(-n, fd);
	}
	else
	{
		d = n % 10 + '0';
		if (n / 10 > 0)
			ft_putnbr_fd(n / 10, fd);
		write(fd, &d, 1);
	}
}
